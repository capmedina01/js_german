/*
Los ciclos nos permiten repetir instrucciones
CICLO for: lo usamos cuando sabemos el 
número de iteraciones(repeticiones)
primero: valor inicial de la variable iteradora
sengundo: condición del ciclo, si la condición no se
cumple, el ciclo no itera
tercero: el cambio de la variable, puede ser aumentar, decrementar
*/
//Imprimir los números del 1 al 10

for (let index = 1; index <= 10; index++) {
    console.log(index);
}


