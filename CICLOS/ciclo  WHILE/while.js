/*
Los ciclos nos permiten repetir instrucciones
CICLO while: lo usamos cuando no sabemos el 
número de iteraciones(repeticiones)
-Consta de una condición,siempre que la condición
se cumpla, el ciclo se ejecutará. La condición
debe brindar la garantía de que alguna vez no
se va a cumplir.
*/
//Mostrar los números del 1 al 10
//variable tipo contador que controla el ciclo
let contador = 1;
while (contador <= 10) {
    console.log(contador);
    contador++;
}


